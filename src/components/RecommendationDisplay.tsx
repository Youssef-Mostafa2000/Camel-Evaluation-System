import { Sparkles, Clock, X } from 'lucide-react';

interface RecommendationDisplayProps {
  recommendation: string;
  type: 'care' | 'breeding' | 'health' | 'custom';
  loading?: boolean;
  onClose?: () => void;
}

export default function RecommendationDisplay({
  recommendation,
  type,
  loading,
  onClose,
}: RecommendationDisplayProps) {
  const typeColors = {
    care: { bg: 'bg-ocean-50', border: 'border-ocean-200', text: 'text-ocean-700', icon: 'text-ocean-600' },
    breeding: { bg: 'bg-green-50', border: 'border-green-200', text: 'text-green-700', icon: 'text-green-600' },
    health: { bg: 'bg-gold-50', border: 'border-gold-200', text: 'text-gold-700', icon: 'text-gold-600' },
    custom: { bg: 'bg-sand-50', border: 'border-sand-200', text: 'text-sand-700', icon: 'text-sand-600' },
  };

  const colors = typeColors[type];

  const typeLabels = {
    care: 'Care Plan Recommendation',
    breeding: 'Breeding Strategy',
    health: 'Health & Fitness Assessment',
    custom: 'Custom Recommendation',
  };

  if (loading) {
    return (
      <div className={`${colors.bg} border ${colors.border} rounded-xl p-8`}>
        <div className="flex items-center justify-center gap-3">
          <div className="w-6 h-6 border-4 border-gold-500 border-t-transparent rounded-full animate-spin" />
          <p className={`${colors.text} font-medium`}>Generating recommendation...</p>
        </div>
      </div>
    );
  }

  const formatRecommendation = (text: string) => {
    const sections = text.split('\n\n');
    return sections.map((section, idx) => {
      if (section.startsWith('**') && section.includes('**')) {
        const parts = section.split('**');
        return (
          <div key={idx} className="mb-4">
            <h4 className="font-bold text-lg text-brown-800 mb-2">{parts[1]}</h4>
            {parts[2] && <p className="text-gray-700 leading-relaxed whitespace-pre-line">{parts[2]}</p>}
          </div>
        );
      }

      if (section.startsWith('- ') || section.includes('\n- ')) {
        const items = section.split('\n').filter(line => line.trim());
        return (
          <ul key={idx} className="list-disc list-inside space-y-2 mb-4 ml-4">
            {items.map((item, i) => (
              <li key={i} className="text-gray-700 leading-relaxed">
                {item.replace(/^- /, '')}
              </li>
            ))}
          </ul>
        );
      }

      if (section.match(/^\d+\./)) {
        const items = section.split('\n').filter(line => line.trim());
        return (
          <ol key={idx} className="list-decimal list-inside space-y-2 mb-4 ml-4">
            {items.map((item, i) => (
              <li key={i} className="text-gray-700 leading-relaxed">
                {item.replace(/^\d+\.\s*/, '')}
              </li>
            ))}
          </ol>
        );
      }

      return (
        <p key={idx} className="text-gray-700 mb-4 leading-relaxed whitespace-pre-line">
          {section}
        </p>
      );
    });
  };

  return (
    <div className={`${colors.bg} border ${colors.border} rounded-xl p-8 shadow-lg relative`}>
      {onClose && (
        <button
          onClick={onClose}
          className="absolute top-4 right-4 p-2 hover:bg-white rounded-lg transition-colors"
        >
          <X className="w-5 h-5 text-gray-600" />
        </button>
      )}

      <div className="flex items-center gap-3 mb-6 pb-6 border-b border-gray-200">
        <div className={`p-3 rounded-xl bg-gradient-to-br from-gold-500 to-gold-600 text-white`}>
          <Sparkles className="w-6 h-6" />
        </div>
        <div>
          <h3 className="text-2xl font-bold text-brown-800">{typeLabels[type]}</h3>
          <p className="text-sm text-gray-600 flex items-center gap-1 mt-1">
            <Clock className="w-4 h-4" />
            Generated just now
          </p>
        </div>
      </div>

      <div className="prose prose-lg max-w-none">
        {formatRecommendation(recommendation)}
      </div>

      <div className="mt-8 pt-6 border-t border-gray-200">
        <div className="flex items-start gap-3 p-4 bg-yellow-50 border border-yellow-200 rounded-lg">
          <span className="text-yellow-600 text-2xl">ðŸ’¡</span>
          <div className="flex-1">
            <p className="text-sm text-yellow-800 font-medium mb-1">
              AI-Generated Recommendation
            </p>
            <p className="text-xs text-yellow-700">
              This recommendation is generated by AI and should be used as guidance. Always consult with veterinary and breeding experts for professional advice.
            </p>
          </div>
        </div>
      </div>
    </div>
  );
}
